  /// Strips Markdown and LaTeX delimiters from [text] so it renders cleanly
  /// in PDF. LaTeX math content is converted to readable Unicode via
  /// [_latexToText].
  static String _strip(String text) {
    final stripped = text
        // LaTeX: display mode $$...$$ must come before inline $...$
        .replaceAllMapped(
          RegExp(r'\$\$(.+?)\$\$', dotAll: true),
          (m) => _latexToText(m.group(1)!),
        )
        // LaTeX: inline $...$
        .replaceAllMapped(
          RegExp(r'\$(.+?)\$', dotAll: true),
          (m) => _latexToText(m.group(1)!),
        )
        // LaTeX: \(...\) inline
        .replaceAllMapped(
          RegExp(r'\\\((.+?)\\\)', dotAll: true),
          (m) => _latexToText(m.group(1)!),
        )
        // LaTeX: \[...\] display
        .replaceAllMapped(
          RegExp(r'\\\[(.+?)\\\]', dotAll: true),
          (m) => _latexToText(m.group(1)!),
        )
        // Markdown: **bold** and __bold__
        .replaceAllMapped(
          RegExp(r'\*\*(.+?)\*\*', dotAll: true),
          (m) => m.group(1)!,
        )
        .replaceAllMapped(
          RegExp(r'__(.+?)__', dotAll: true),
          (m) => m.group(1)!,
        )
        // Markdown: *italic* — use word-boundary-aware pattern to avoid
        // conflicting with underscores produced by LaTeX subscript conversion.
        .replaceAllMapped(
          RegExp(r'\*(.+?)\*', dotAll: true),
          (m) => m.group(1)!,
        )
        // Markdown: `code`
        .replaceAllMapped(RegExp(r'`(.+?)`', dotAll: true), (m) => m.group(1)!)
        // Markdown: [text](url)
        .replaceAllMapped(RegExp(r'\[(.+?)\]\(.+?\)'), (m) => m.group(1)!)
        // Markdown: # headings
        .replaceAll(RegExp(r'^#{1,6}\s+', multiLine: true), '');
    return _sanitizeUnicode(stripped.trim());
  }

