  static List<pw.Widget> _buildQuestionsPage(
    List<Question> questions,
    _PdfContext labels,
    Map<String, Uint8List> latexImages,
  ) {
    final widgets = <pw.Widget>[
      pw.Outline(
        name: 'questions_section',
        title: labels.questionsPageTitle,
        level: 0,
        child: pw.Text(
          labels.questionsPageTitle,
          style: pw.TextStyle(fontSize: 20, fontWeight: pw.FontWeight.bold),
        ),
      ),
      pw.SizedBox(height: 8),
      pw.Divider(thickness: 2, color: PdfColors.grey600),
      pw.SizedBox(height: 16),
    ];

    for (int i = 0; i < questions.length; i++) {
      final q = questions[i];
      widgets.add(
        pw.Padding(
          padding: const pw.EdgeInsets.only(bottom: 12),
          child: pw.Column(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Text(
                '${i + 1}. ${_strip(q.text)}',
                style:
                    pw.TextStyle(fontSize: 11, fontWeight: pw.FontWeight.bold),
              ),
              ..._inlineEquationWidgets(q.text, latexImages),
              if (q.options.isNotEmpty) ...[
                pw.SizedBox(height: 4),
                ...List.generate(q.options.length, (j) {
                  final label = String.fromCharCode(65 + j); // A, B, C…
                  return pw.Column(
                    crossAxisAlignment: pw.CrossAxisAlignment.start,
                    children: [
                      pw.Padding(
                        padding: const pw.EdgeInsets.only(left: 16, bottom: 2),
                        child: pw.Row(
                          crossAxisAlignment: pw.CrossAxisAlignment.start,
                          children: [
                            pw.Text(
                              '$label. ',
                              style: const pw.TextStyle(fontSize: 10),
                            ),
                            pw.Expanded(
                              child: pw.Text(
                                _strip(q.options[j]),
                                style: const pw.TextStyle(fontSize: 10),
                              ),
                            ),
                          ],
                        ),
                      ),
                      ..._inlineEquationWidgets(
                        q.options[j],
                        latexImages,
                        paddingLeft: 32,
                      ),
                    ],
                  );
                }),
              ],
            ],
          ),
        ),
      );
    }

    return widgets;
  }

  static List<pw.Widget> _buildAnswersPage(
    List<Question> questions,
    _PdfContext labels,
    Map<String, Uint8List> latexImages,
  ) {
    final widgets = <pw.Widget>[
      pw.Outline(
        name: 'answers_section',
        title: labels.answersLabel,
        level: 0,
        child: pw.Text(
          labels.answersLabel,
          style: pw.TextStyle(fontSize: 20, fontWeight: pw.FontWeight.bold),
        ),
      ),
      pw.SizedBox(height: 8),
      pw.Divider(thickness: 2, color: PdfColors.grey600),
      pw.SizedBox(height: 16),
    ];

    for (int i = 0; i < questions.length; i++) {
      final q = questions[i];
      final correctLabels = q.correctAnswers
          .where((idx) => idx < q.options.length)
          .map(
            (idx) =>
                '${String.fromCharCode(65 + idx)}. ${_strip(q.options[idx])}',
          )
          .toList();

      widgets.add(
        pw.Padding(
          padding: const pw.EdgeInsets.only(bottom: 10),
          child: pw.Row(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Text(
                '${i + 1}. ',
                style:
                    pw.TextStyle(fontSize: 10, fontWeight: pw.FontWeight.bold),
              ),
              pw.Expanded(
                child: pw.Column(
                  crossAxisAlignment: pw.CrossAxisAlignment.start,
                  children: [
                    pw.Text(
                      _strip(q.text),
                      style: const pw.TextStyle(
                        fontSize: 10,
                        color: PdfColors.grey700,
                      ),
                    ),
                    ..._inlineEquationWidgets(q.text, latexImages),
                    pw.SizedBox(height: 3),
                    if (correctLabels.isNotEmpty)
                      ...q.correctAnswers
                          .where((idx) => idx < q.options.length)
                          .expand((idx) => [
                            pw.Text(
                              '${String.fromCharCode(65 + idx)}. ${_strip(q.options[idx])}',
                              style: pw.TextStyle(
                                fontSize: 10,
                                fontWeight: pw.FontWeight.bold,
                                color: PdfColors.green800,
                              ),
                            ),
                            ..._inlineEquationWidgets(
                              q.options[idx],
                              latexImages,
                            ),
                          ])
                    else if (q.explanation.isNotEmpty) ...[
                      pw.Text(
                        _strip(q.explanation),
                        style: pw.TextStyle(
                          fontSize: 10,
                          fontWeight: pw.FontWeight.bold,
                          color: PdfColors.green800,
                        ),
                      ),
                      ..._inlineEquationWidgets(q.explanation, latexImages),
                    ],
                  ],
                ),
              ),
            ],
          ),
        ),
      );
    }

    return widgets;
  }

  /// Returns image widgets for any inline LaTeX equations found in [text] that
  /// have a pre-rendered entry in [latexImages]. Equations with no pre-rendered
  /// image are already shown as plain text via [_strip], so they are skipped.
  static List<pw.Widget> _inlineEquationWidgets(
    String text,
    Map<String, Uint8List> latexImages, {
    double paddingLeft = 0,
  }) {
    final widgets = <pw.Widget>[];
    final patterns = [
      RegExp(r'\$\$(.+?)\$\$', dotAll: true),
      RegExp(r'(?<!\$)\$(?!\$)(.+?)(?<!\$)\$(?!\$)', dotAll: true),
      RegExp(r'\\\[(.+?)\\\]', dotAll: true),
      RegExp(r'\\\((.+?)\\\)', dotAll: true),
    ];
    final seen = <String>{};
    for (final pattern in patterns) {
      for (final match in pattern.allMatches(text)) {
        final eq = match.group(1)?.trim() ?? '';
        if (eq.isEmpty || seen.contains(eq)) continue;
        seen.add(eq);
        final bytes = latexImages[eq];
        if (bytes == null) continue;
        widgets.add(
          pw.Padding(
            padding: pw.EdgeInsets.only(left: paddingLeft, top: 4, bottom: 4),
            child: pw.Center(child: pw.Image(pw.MemoryImage(bytes))),
          ),
        );
      }
    }
    return widgets;
  }

  static pw.Widget _buildCoverSection(String title, String? summary) {
    return pw.Center(
      child: pw.Column(
        mainAxisAlignment: pw.MainAxisAlignment.center,
        crossAxisAlignment: pw.CrossAxisAlignment.center,
        mainAxisSize: pw.MainAxisSize.min,
        children: [
          pw.Text(
            title,
            textAlign: pw.TextAlign.center,
            style: pw.TextStyle(fontSize: 40, fontWeight: pw.FontWeight.bold),
          ),
          if (summary != null && summary.isNotEmpty) ...[
            pw.SizedBox(height: 20),
            pw.SizedBox(
              width: 400,
              child: pw.Text(
                _strip(summary),
                textAlign: pw.TextAlign.center,
                style: pw.TextStyle(
                  fontSize: 14,
                  fontStyle: pw.FontStyle.italic,
                  color: PdfColors.grey600,
                ),
              ),
            ),
          ],
        ],
      ),
    );
  }

  static List<pw.Widget> _buildChunks(
    List<StudyChunk> chunks,
    _PdfContext labels,
  ) {
    final readyChunks = chunks
        .where(
          (c) =>
              c.status == StudyChunkState.completed ||
              c.status == StudyChunkState.downloaded,
        )
        .toList();

    final widgets = <pw.Widget>[];
    for (int i = 0; i < readyChunks.length; i++) {
      widgets.addAll(_buildChunk(readyChunks[i], i, labels));
    }
    return widgets;
  }

  static List<pw.Widget> _buildChunk(
    StudyChunk chunk,
    int index,
    _PdfContext labels,
  ) {
    final widgets = <pw.Widget>[];

    final chunkLabel = '${index + 1}. ${_strip(chunk.title)}';
    widgets.add(
      pw.Outline(
        name: 'chunk_$index',
        title: chunkLabel,
        level: 0,
        child: pw.Text(
          chunkLabel,
          style: pw.TextStyle(fontSize: 18, fontWeight: pw.FontWeight.bold),
        ),
      ),
    );

    /* Note: AI summaries excluded
     if (chunk.aiSummary != null && chunk.aiSummary!.isNotEmpty) {
      widgets.add(pw.SizedBox(height: 4));
      widgets.add(
        pw.Text(
          _strip(chunk.aiSummary!),
          style: pw.TextStyle(
            fontSize: 11,
            fontStyle: pw.FontStyle.italic,
            color: PdfColors.grey700,
          ),
        ),
      );
    }
    */

    widgets.add(pw.SizedBox(height: 8));
    widgets.add(pw.Divider(color: PdfColors.grey400));
    widgets.add(pw.SizedBox(height: 8));

    for (final page in chunk.pages) {
      for (final component in page.uiElements) {
        widgets.addAll(_buildComponent(component, labels));
      }
    }

    widgets.add(pw.SizedBox(height: 16));

    return widgets;
  }

  static List<pw.Widget> _buildComponent(
    StudyComponent component,
    _PdfContext labels,
  ) {
    switch (component.componentType) {
      case StudyComponentType.sectionTitle:
        return _buildSectionTitle(component);
      case StudyComponentType.paragraph:
        return _buildParagraph(component);
      case StudyComponentType.keyDefinition:
        return _buildKeyDefinition(component);
      case StudyComponentType.numberedList:
        return _buildNumberedList(component);
      case StudyComponentType.comparisonTable:
        return _buildComparisonTable(component);
      case StudyComponentType.quote:
        return _buildQuote(component);
      case StudyComponentType.warning:
        return _buildWarning(component);
      case StudyComponentType.formula:
        return _buildFormula(component, labels);
      case StudyComponentType.timeline:
        return _buildTimeline(component);
      case StudyComponentType.prosCons:
        return _buildProsCons(component, labels);
      case StudyComponentType.keyConcepts:
        return _buildKeyConcepts(component);
      case StudyComponentType.reminder:
        return _buildReminder(component);
      case StudyComponentType.iconCards:
        return _buildIconCards(component);
    }
  }

