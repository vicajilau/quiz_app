/// Bundles localizable labels and pre-rendered LaTeX images needed during PDF
/// generation.
class _PdfContext {
  final String advantages;
  final String limitations;
  final String tableOfContents;
  final String questionsPageTitle;
  final String answersLabel;

  /// Maps raw equation strings to pre-rendered PNG bytes captured via
  /// [LaTeXImageRenderer]. When an entry is present the equation is embedded
  /// as an image instead of being converted to plain text.
  final Map<String, Uint8List> latexImages;

  const _PdfContext({
    required this.advantages,
    required this.limitations,
    required this.tableOfContents,
    this.questionsPageTitle = 'Questions',
    this.answersLabel = 'Answer',
    this.latexImages = const {},
  });
}

/// Generates a PDF document from study data.
///
/// This class is platform-independent and contains only the PDF generation
/// logic using the `pdf` package. Saving the result to disk is handled by
/// the platform-specific [StudyPdfExportService].
class StudyPdfGenerator {
  StudyPdfGenerator._();

  /// Generates a PDF document from the provided study data and returns the
  /// bytes of the resulting file.
  ///
  /// [advantagesLabel], [limitationsLabel] and [tableOfContentsLabel] are the
  /// localized strings used in the PDF. They default to English if omitted.
  static Future<Uint8List> generate({
    required String documentTitle,
    String? documentSummary,
    required List<StudyChunk> chunks,
    String advantagesLabel = 'Advantages',
    String limitationsLabel = 'Limitations',
    String tableOfContentsLabel = 'Table of Contents',
    Map<String, Uint8List> latexImages = const {},
    List<Question> questions = const [],
    bool includeAnswers = false,
    String questionsPageTitle = 'Questions',
    String answersLabel = 'Answer',
  }) async {
    final labels = _PdfContext(
      advantages: advantagesLabel,
      limitations: limitationsLabel,
      tableOfContents: tableOfContentsLabel,
      questionsPageTitle: questionsPageTitle,
      answersLabel: answersLabel,
      latexImages: latexImages,
    );

    final pdf = pw.Document();

    final img1 = await rootBundle.load('images/pictorial_mark.jpg');
    final imageBytes1 = img1.buffer.asUint8List();

    final img2 = await rootBundle.load('images/Quizdy.jpg');
    final imageBytes2 = img2.buffer.asUint8List();

    final pageTheme = pw.PageTheme(
      pageFormat: PdfPageFormat.a4,
      buildBackground: (context) => pw.Container(
        color: PdfColors.white,
        child: pw.Center(
          child: pw.Opacity(
            opacity: 0.1,
            child: pw.Column(
              mainAxisSize: pw.MainAxisSize.min,
              children: [
                pw.Image(pw.MemoryImage(imageBytes1), height: 240),
                pw.Image(pw.MemoryImage(imageBytes2), height: 180),
              ],
            ),
          ),
        ),
      ),
      margin: const pw.EdgeInsets.symmetric(horizontal: 48, vertical: 40),
    );

    pw.Widget buildFooter(pw.Context context) => pw.Container(
      margin: const pw.EdgeInsets.only(top: 20),
      child: pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        children: [
          pw.Row(
            children: [
              pw.Image(pw.MemoryImage(imageBytes1), width: 18, height: 18),
              pw.SizedBox(width: 6),
              pw.Text(
                'Generated by Quizdy',
                style: const pw.TextStyle(
                  fontSize: 10,
                  color: PdfColors.grey700,
                ),
              ),
            ],
          ),
          pw.Text(
            context.pageNumber.toString(),
            style: const pw.TextStyle(fontSize: 10, color: PdfColors.grey700),
          ),
        ],
      ),
    );

    final readyChunks = chunks
        .where(
          (c) =>
              c.status == StudyChunkState.completed ||
              c.status == StudyChunkState.downloaded,
        )
        .toList();

    // Page 1: Cover — full-page centered layout, no footer.
    pdf.addPage(
      pw.Page(
        pageTheme: pageTheme,
        build: (context) => _buildCoverSection(documentTitle, documentSummary),
      ),
    );

    // Pages 2+: Content — added before the TOC page so that pw.Outline
    // widgets register their PdfPage references before the TOC's postProcess.
    pdf.addPage(
      pw.MultiPage(
        pageTheme: pageTheme,
        footer: buildFooter,
        build: (pw.Context context) => _buildChunks(chunks, labels),
      ),
    );

    // TOC page: physically inserted at index 1 (right after the cover) but
    // added to the processing queue last so all Outline entries are resolved.
    // pw.TableOfContent uses DelayedWidget internally to read page numbers
    // after the full document outline is built.
    if (readyChunks.isNotEmpty || questions.isNotEmpty) {
      pdf.addPage(
        pw.Page(
          pageTheme: pageTheme,
          build: (context) => pw.Column(
            crossAxisAlignment: pw.CrossAxisAlignment.start,
            children: [
              pw.Expanded(
                child: pw.Column(
                  crossAxisAlignment: pw.CrossAxisAlignment.start,
                  children: [
                    pw.Text(
                      labels.tableOfContents,
                      style: pw.TextStyle(
                        fontSize: 20,
                        fontWeight: pw.FontWeight.bold,
                      ),
                    ),
                    pw.SizedBox(height: 8),
                    pw.Divider(thickness: 2, color: PdfColors.grey600),
                    pw.SizedBox(height: 16),
                    pw.TableOfContent(),
                  ],
                ),
              ),
              buildFooter(context),
            ],
          ),
        ),
        index: 1,
      );
    }

    // Questions page: appended at the end when requested.
    if (questions.isNotEmpty) {
      pdf.addPage(
        pw.MultiPage(
          pageTheme: pageTheme,
          footer: buildFooter,
          build: (pw.Context context) =>
              _buildQuestionsPage(questions, labels, latexImages),
        ),
      );
    }

    // Answers page: separate page after questions when requested.
    if (questions.isNotEmpty && includeAnswers) {
      pdf.addPage(
        pw.MultiPage(
          pageTheme: pageTheme,
          footer: buildFooter,
          build: (pw.Context context) =>
              _buildAnswersPage(questions, labels, latexImages),
        ),
      );
    }

    return pdf.save();
  }

